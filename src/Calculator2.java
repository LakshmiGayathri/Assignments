
public class Calculator2 {
private static int a;
private static int b;

  public void setAdd(int a,int b)
  {
	this.a=a;
	this.b=b;
	}
  
  public int getAdd()
  {
	return a+b;
	
	}

 
  public void setProduct(int a,int b)
  {
	  this.a=a;
	  this.b=b;
   }
  
  public int getProduct()
  {
	return a*b;
	
	}

	
}


